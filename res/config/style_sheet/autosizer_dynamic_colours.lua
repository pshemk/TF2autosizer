local function getColourString(r, g, b)
    local x = string.format("%03.0f", (r * 100))
    local y = string.format("%03.0f", (g * 100))
    local z = string.format("%03.0f", (b * 100))
    return x .. y .. z
end

function data()
    local result = { }

    local a = ssu.makeAdder(result)          -- helper function

    for _, colour in pairs(defaultColours) do
        local colourString = getColourString(colour[1], colour[2], colour[3])
        local colourArray = {colour[1], colour[2], colour[3], 1}

        a("!asrLineColour-" .. colourString, {
            color = colourArray,
            fontSize = 27,
            padding = {0, 3, 0 ,3},
        })
        a("!asrBackgroundLineColour-" .. colourString, {
            backgroundColor = colourArray,
            fontSize = 5,
            padding = {0, 0, 0 ,0},
            margin = {0, 0, 0 ,0},
        })

    end

    return result
end